#include "libft.h"

int		ft_char_index(const char *str, char q)
{
	int i;

	i = 0;
	if (!str)
		return (-1);
	while (str[i] != '\0')
	{
		if (str[i] == q)
			return (i);
		i++;
	}
	return (-1);
}
